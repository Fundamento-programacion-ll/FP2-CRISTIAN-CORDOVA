    private void mostrar() {          
        modelo = (DefaultTableModel)tbl_tabla.getModel();
        List<autos> lista = autocontrolador.listarautos();
        Object[] objeto = new Object[4];
        for (int i = 0; i < lista.size(); i++) {
            objeto[0] = lista.get(i).getMatricula();
            objeto[1] = lista.get(i).getCedula();
            objeto[2] = lista.get(i).getModelo();
            objeto[3] = lista.get(i).getNombreDueño();
            modelo.addRow(objeto);
        }
                  
    }
    
     public void limpiarTabla() {
        for (int i = 0; i < tbl_tabla.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }
    
