    private void bot_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_modificarActionPerformed
        // TODO add your handling code here:
        int fila = tbl_tabla.getSelectedRow();
        if(fila>=0){
            txt_matricula.setText(tbl_tabla.getValueAt(fila,0).toString());
            txt_cedula.setText(tbl_tabla.getValueAt(fila,1).toString());
            txt_modelo.setText(tbl_tabla.getValueAt(fila,2).toString());
            txt_nombreD.setText(tbl_tabla.getValueAt(fila,3).toString());

        }else{
            JOptionPane.showMessageDialog(null, "No selecciono ninguna fila");
        }
    }//GEN-LAST:event_bot_modificarActionPerformed

    private void bot_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_actualizarActionPerformed
        // TODO add your handling code here:
        if(txt_cedula.getText().equals("") || txt_matricula.getText().equals("")
                || txt_modelo.getText().equals("") || txt_nombreD.getText().equals("")){
           
              lbl_error.setText("ingrese toda la informacion");
              }else {
    
                if (!txt_cedula.getText().trim().equals("")
                && JOptionPane.showConfirmDialog(this, "Seguro que desea guardar datos?", "modificar auto", 
                        JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
            
                    
                    autos au = new autos(
                    
                    txt_matricula.getText(),
                    txt_modelo.getText(),
                    txt_nombreD.getText(),
                    Integer.parseInt(txt_cedula.getText())
                    
            );
            autocontrolador.ModificarDatos(au);
            
         }
             }
    }//GEN-LAST:event_bot_actualizarActionPerformed

