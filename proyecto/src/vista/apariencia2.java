     private void mostrar() {          
        modelo = (DefaultTableModel)tbl_apariencia.getModel();
        List<apariencia> lista = autocontrolador.listarapariencia();
        Object[] objeto = new Object[5];
        for (int i = 0; i < lista.size(); i++) {
            objeto[0] = lista.get(i).getCedulaCo();
            objeto[1] = lista.get(i).getCedulaRe();
            objeto[2] = lista.get(i).getColor();
            objeto[3] = lista.get(i).getModificacion();
            objeto[4] = lista.get(i).getNombreRe();
            modelo.addRow(objeto);
        }
                  
    }
     
     public void limpiarTabla() {
        for (int i = 0; i < tbl_apariencia.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }
    
    
