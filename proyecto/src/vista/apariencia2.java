    private void bot_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_actualizarActionPerformed
        // TODO add your handling code here:
        if(txt_cedulaCo.getText().equals("") || txt_cedulaRE.getText().equals("")
                || txt_color.getText().equals("") || txt_modificacion.getText().equals("") || txt_nombreRe.getText().equals("")){
           
              lbl_error.setText("ingrese toda la informacion");
              }else {
    
                if (!txt_cedulaCo.getText().trim().equals("")
                && JOptionPane.showConfirmDialog(this, "Seguro que desea actualizar datos?", "modificar apariencia", 
                        JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
            
                    
                   apariencia ap = new apariencia(
                    
                    Integer.parseInt(txt_cedulaCo.getText()),
                    Integer.parseInt(txt_cedulaRE.getText()),
                    txt_color.getText(),
                    txt_modificacion.getText(),
                    txt_nombreRe.getText()
                    
            );
            autocontrolador.ModificarDatos(ap);
            
         }
             }
    }//GEN-LAST:event_bot_actualizarActionPerformed

