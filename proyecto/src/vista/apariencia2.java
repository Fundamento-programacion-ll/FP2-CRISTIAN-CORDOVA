    private void bot_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_modificarActionPerformed
        // TODO add your handling code here:
        int fila = tbl_apariencia.getSelectedRow();
        if(fila>=0){
            txt_cedulaCo.setText(tbl_apariencia.getValueAt(fila,0).toString());
            txt_cedulaRE.setText(tbl_apariencia.getValueAt(fila,1).toString());
            txt_color.setText(tbl_apariencia.getValueAt(fila,2).toString());
            txt_modificacion.setText(tbl_apariencia.getValueAt(fila,3).toString());
            txt_nombreRe.setText(tbl_apariencia.getValueAt(fila,4).toString());

        }else{
            JOptionPane.showMessageDialog(null, "No selecciono ninguna fila");
        }
    }//GEN-LAST:event_bot_modificarActionPerformed

    private void bot_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_eliminarActionPerformed
        // TODO add your handling code here:
         int fila = tbl_apariencia.getSelectedRow();

         int  confirmar = JOptionPane.showConfirmDialog(null, "desea eliminar el dato");
             if(confirmar==JOptionPane.OK_OPTION){
         String cod="";
         cod= tbl_apariencia.getValueAt(fila, 0).toString();
         
         autocontrolador.Eliminar(cod);
         
         if(fila > 0){
             
             JOptionPane.showMessageDialog(null, "Dato eliminado");
             modelo.removeRow(fila);
            
         }else{
             JOptionPane.showMessageDialog(null, "no selecciono ninguna fila");
         }
               
               int filaSeleccionada = tbl_apariencia.getSelectedRow();
             }
    }//GEN-LAST:event_bot_eliminarActionPerformed

